#include <stdio.h>
void  G(int n, int k){
    if (n == 1) {
        printf("%d\n", k);
    } 
    else if (k<pow(2, n-1)){
        printf("0");
        G(n-1, k);
    } 
    else if(k>=pow(2, n-1)){
        printf("1");
        G(n-1, pow(2,n)-1-k);
    }
}



int main(){
    int input=0;
    while(input!= -1){
        int n, k;
        scanf("%d%d", &n, &k);
        G(n, k);
        scanf("%d", &input);
    }
}


// 009.葛雷碼 (Gray code) 

// 反射二進位編碼-葛雷碼 (Gray code)，是編碼成兩個連續的不同位元 
// 其編碼規則如下 
// G_1 = {0, 1} 
// G_1_r = {1, 0} 
// G_n = {0G_(n-1), 1G_(n-1)_r } 
// [G_n_r 是 G_n 的逆向順序] 
// 例 : 
// G_2 = {0G_1, 1G_1_r} = {00, 01, 11, 10} 
// G_2_r = {10, 11, 01, 00} 

// ------------- 
// 輸入說明 : 
// 第一行是一個測試案例資料，整數 n k 
// n代表Gray code的位元數 
// k代表G_n中的第幾個元素(從0開始) 
// 接著是一行 0 分隔測試資料 
// 第三行是第二個測試案例資料 
// .... 
// 最後 -1 結束 

// ------------- 
// 輸出說明 : 
// 二進位 Gray code 
// 每一行是一個測試案例資料的結果 

// ------------- 
// Sample Input: 
// 1 1 
// 0 
// 2 3 
// 0 
// 3 6 
// 0 
// 4 12 
// -1 
// ------------- 
// Sample Output: 
// 1 
// 10 
// 101 
// 1010 

// ------------- 
// Sample Input: 
// 5 3 
// 0 
// 6 10 
// -1 
// ------------- 
// Sample Output: 
// 00010 
// 001111



